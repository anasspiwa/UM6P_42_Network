int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	c;

	i = 0;
	c = 1;
	while (*(str + i))
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			c = 1;
		else
			return (0);
		++i;
	}
	return (c);
}
